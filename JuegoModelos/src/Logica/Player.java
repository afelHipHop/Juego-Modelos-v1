package Logica;

/**
 *
 * @author personal
 */
public class Player implements IPlayer{
    
    private int power;
    private String name;
    private int life;

    public Player(int power, String name, int life) {
        this.power = power;
        this.name = name;
        this.life = life;
    }

    @Override
    public int getPower() {
        return power;
    }

    @Override
    public void setPower(int power) {
        this.power = power;
    }

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int getLife() {
        return life;
    }

    @Override
    public void setLife(int life) {
        this.life = life;
    }
    

}
